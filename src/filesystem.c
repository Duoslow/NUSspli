#include <wut-fixups.h>

#include <file.h>
#include <filesystem.h>

static int usb = -1;

int getUSB()
{
	if(usb == -1)
	{
		if(dirExists("fs:/vol/storage_usb01/usr"))
			usb = 1;
		else if(dirExists("fs:/vol/storage_usb02/usr"))
			usb = 2;
		else
			usb = 0;
	}

	return usb;
}
